    public function getResourceList(): array
    {
        $list = [];
        $albums = $this->getAlbums();
        foreach ($albums as $index => $album) {
            $keyword = 't' . str_pad($index + 1, 3, '0', STR_PAD_LEFT);
            $list[] = [
                'keyword' => $keyword,
                'title' => $album['title'],
            ];
        }

        return $list;
    }

    public function resolve(string $keyword): ?ResourceResponse
    {
        // t001-t369+001
        if (Str::startsWith($keyword, 't') && strlen($keyword) >= 4) {
            $cacheKey = 'xbot.tingdao.'.$keyword;
            $data = Cache::get($cacheKey);

            if (! $data) {
                $albums = $this->getAlbums();
                $oriKeyword = substr($keyword, 1, 3);
                $indexSuffix = substr($keyword, 4);
                $albumIdRaw = (int) preg_replace('/[^0-9]/', '', $oriKeyword);
                $albumIndexRaw = (int) preg_replace('/[^0-9]/', '', $indexSuffix);
                $total = count($albums);

                // Original logic: $albumId = abs($albumId-1%$total); $album = $albums[$albumId%$total];
                $computedId = abs($albumIdRaw - 1) % $total;
                $album = $albums[$computedId];

                $id = $album['id'];
                $url = "https://pub-6de883f3fd4a43c28675e9be668042c2.r2.dev/{$id}/{$id}.json";

                try {
                    $response = Http::get($url);
                    if ($response->failed()) {
                        return null;
                    }
                    $json = $response->json();
                    $count = count($json['list']);
                    $image = $json['details'][0]['img_url'];
                    $title = $json['details'][0]['title'];

                    if (strlen($keyword) == 4) {
                        $key = date('z') % $count;
                    } else {
                        $key = abs(($albumIndexRaw - 1) % $count);
                    }

                    $item = $json['list'][$key];
                    $description = str_replace($title, '', $item['title']);

                    $mp3 = basename(parse_url($item['video_url'], PHP_URL_PATH));
                    $audioUrl = "https://tingdao.simai.life/{$id}/{$mp3}";

                    $displayKeyword = 't'.str_pad($computedId + 1, 3, '0', STR_PAD_LEFT);
                    $data = [
                        'url' => $audioUrl,
                        'title' => "【{$displayKeyword}】".$title,
                        'description' => ($key + 1)."/$count 每日更新  $description",
                        'image' => $image,
                    ];

                    Cache::put($cacheKey, $data, strtotime('tomorrow') - time());
                } catch (\Exception $e) {
                    return null;
                }
            }

            return ResourceResponse::music($data);
        }

        return null;
    }

